// DJAX
if(window.history.pushState != undefined){
	function _djaxNavigate(url, add){
		$("#results").css("opacity", "0.4");
		$.ajax({
			url: url,
			dataType: 'html',
			beforeSend: function(xhr){
				xhr.setRequestHeader('X-PJAX', 'true');
				$(".loading").show();
				$('html, body').stop().animate({
					scrollTop: $("#resultsHeader").offset().top
				}, 1500,'easeInOutExpo');
			},
			success: function(data){
				$('#results').html(data).css("opacity", "1");
				$(".loading").fadeOut();

				$(document).trigger("djax");
				if(add){
					history.pushState(null, null, url);
				}
			}
		});
	}
	window['djaxNavigate'] = function(url){
		_djaxNavigate(url, true);
	}

	$(window).bind("popstate", function(event){
		try{ _djaxNavigate(event.originalEvent.state.url, false); }
		catch(e){}
	});
} else{
	window['djaxNavigate'] = function(url){
		document.location.href = url;
	}
}

